    Iдея створення Всеукраїнської молодіжної громадської організації (ВМГО «МЦП») виникла серед директорів обласних Молодіжних центрів праці в 2004 році, як певного громадського об’єднання, яке б відстоювало інтереси членів цього об’єднання, а саме: керівників та працівників молодіжних центрів праці, бізнес-інкубаторів, бізнес-центрів, членів трудових загонів, громадян, які займаються постійним і тимчасовим працевлаштуванням, профорієнтацією молоді та розвитком молодіжного підприємництва.
